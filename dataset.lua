    for _, ex in ipairs(dataset.raw) do
        for _, tok in ipairs(tokenize_with_bigrams(ex[1])) do
            freq[tok] = (freq[tok] or 0) + 1
        end
    end
    local vocab, word2idx = {}, {}
    for w, _ in pairs(freq) do
        vocab[#vocab + 1] = w
    end
    table.sort(vocab)
    for i, w in ipairs(vocab) do
        word2idx[w] = i
    end
    return vocab, word2idx
end

-- ─── Bag-of-words + bigram encoder ───────────────────────────────────────────

function dataset.encode(text, word2idx, vocab_size)
    local vec = {}
    for i = 1, vocab_size do vec[i] = 0.0 end
    for _, tok in ipairs(tokenize_with_bigrams(text)) do
        local idx = word2idx[tok]
        if idx then vec[idx] = 1.0 end
    end
    return vec
end

-- ─── One-hot target ───────────────────────────────────────────────────────────

function dataset.one_hot(intent)
    local n   = #dataset.intents
    local vec = {}
    for i = 1, n do vec[i] = 0.0 end
    local idx = dataset.intent_index[intent]
    if idx then vec[idx] = 1.0 end
    return vec
end

-- ─── Build full training set ──────────────────────────────────────────────────

function dataset.build(word2idx, vocab_size)
    local out = {}
    for _, ex in ipairs(dataset.raw) do
        out[#out + 1] = {
            input  = dataset.encode(ex[1], word2idx, vocab_size),
            target = dataset.one_hot(ex[2]),
        }
    end
    return out
end

return dataset