-- ─── Vocabulary builder ───────────────────────────────────────────────────────

local STOPWORDS = {
    a=true, an=true, the=true, is=true, it=true, to=true,
    ["do"]=true, i=true, me=true, my=true, we=true, you=true,
    your=true, ["in"]=true, of=true, ["and"]=true, ["or"]=true, be=true,
    am=true, are=true, was=true, have=true, has=true, ["for"]=true,
}

local function tokenize(text)
    local tokens = {}
    local clean = text:lower():gsub("[^%a%s]", " ")
    for w in clean:gmatch("%S+") do
        if not STOPWORDS[w] then
            tokens[#tokens + 1] = w
        end
    end
    return tokens
end

local function tokenize_with_bigrams(text)
    local tokens = tokenize(text)
    local result = {}
    for _, t in ipairs(tokens) do result[#result + 1] = t end
    for i = 1, #tokens - 1 do
        result[#result + 1] = tokens[i] .. "_" .. tokens[i + 1]
    end
    return result
end

